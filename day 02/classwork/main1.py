# მათემატიკური ოპერატორები - Mathematical operators.

# + მიმატება -- Addition
# - გამოკლება -- Substraction
# * გმარავლება -- Multiplication
# / გაყოფა -- Division

# + მიმატება -- Addition
print(25 + 10) # output: 35 (გამოტანილი ინფორმაცია)

# - გამოკლება -- Substraction
print(10 - 5)

# * გამრავლება -- Multiplication
print(5 * 5)

# / გაყოფა -- Division 
print(10 / 2)

# როდესაც ორ ინტეჯერს ვყოფთ ერთმანეთზე, ამ გაყოფის შედეგი ყოველთვის იქნება Float.