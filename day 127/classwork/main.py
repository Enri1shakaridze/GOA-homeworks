# მოიფიქრეთ მინიმუმ 2 მაგალითი Multi-level inheritence-ზე. 

# 1) მაგ. შევქმნათ car კლასი მისი შვილი კლასი იყოს sedan და sedan-ს შვილი ელემენტი იყოს electrocar და ა.შ.
# car => sedan => electrocar

# 2) მაგ. შევქმნათ person კლასი შემდეგ შვილი ელემენტი იყოს Employee და Employee ს შვილი ელემენტი იყოს manager
# person => Employee => manager

