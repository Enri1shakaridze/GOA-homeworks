# <    ნაკლებია
# >    მეტია
# ==   ტოლია
# !=   არ არის ტოლი
# <=   ნაკლებია ან ტოლია
# >=   მეტია ან ტოლია


# ნაკლებია (<)
print(5 < 10)     # True
print(8 < 3)      # False

# მეტია (>)
print(7 > 4)      # True
print(2 > 9)      # False

# ტოლია (==)
print(3 == 3)     # True
print(5 == 2)     # False

# არ არის ტოლია (!=)
print(6 != 4)     # True
print(10 != 10)   # False

# ნაკლებია ან ტოლია (<=)
print(4 <= 4)     # True
print(3 <= 1)     # False

# მეტია ან ტოლია (>=)
print(9 >= 9)     # True
print(2 >= 5)     # False



num1 = "40"     # ეს არის ტექსტი (სტრინგი)
num2 = 40       # ეს არის რიცხვი (ინტი)
print(num1 == num2)
# გამოიტანს False ს რადგან num1 წერია სტრინგათ და num2 წერია ინტეჯერათ



soil_moisture = 80
limit = 100
print(soil_moisture != limit)
# ეს გამოიტანს True ს რადგან 80 არ უდრის 100

