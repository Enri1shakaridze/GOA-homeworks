# IndentationError — როცა ზოლები (tab/space) არასწორადაა.
# NameError — როცა ცვლადი ან სახელი არ არსებობს.
# TypeError — როცა შეუთავსებელ ტიპებს იყენებ ერთად.
# ValueError — როცა მნიშვნელობა არასწორია.
# IndexError — როცა არარსებულ ინდექსს მიმართავ სიაში.
# KeyError — როცა არარსებულ key-ს ეძებ ლექსიკონში.
# AttributeError — როცა ობიექტს არ აქვს ისეთი მეთოდი ან თვისება.
# ImportError — როცა ვერ იტვირთება მოდული.
# ModuleNotFoundError — როცა მოდული საერთოდ არ არსებობს.
