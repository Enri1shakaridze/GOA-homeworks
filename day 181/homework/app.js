// შექმენი ფაილი counter.js, სადაც გექნება ლოკალური ცვლადი let count = 0, ფუნქცია increment(), რომელიც ამ ცვლადს ერთით ზრდის, და ორივე მათგანს გაიტან named export-ით. მთავარ ფაილში შემოიტანე count და increment, გამოიძახე ფუნქცია რამდენჯერმე და დააკვირდი, როგორ იცვლება შემოტანილი count ცვლადის მნიშვნელობა პირდაპირ მეხსიერებაში.

export let count = 0

export function increment(){
    count++
}
